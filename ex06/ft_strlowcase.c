char *ft_strlowcase(char *str)
{
	int i;
	
	i = 0;
	while(str[i] != '\0')
	{
		if(str[i] >= 'A' && str[i] <= 'z')
		{
			str[i] += -'A' + 'a';
		}
		i++;
	}
	return(str);
}
